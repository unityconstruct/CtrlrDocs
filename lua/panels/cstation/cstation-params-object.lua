---Lookup Param Min/Max by ParaId
---@return table
function getParamsIdsMinMax()
    ParamsIdsMinMax = {}
    ---Fetches Min/Max param values from table by integer id
    ---splits table value by ',' and returns a tuple
    ---@param paramId integer - integer number for the table index to look up
    ---@return number? - min integer. negative is allowed
    ---@return number? - max integer
    ParamsIdsMinMax.GetMinMax = function(paramId)
        local haystackString = ParamsIdsMinMax[tonumber(paramId)]
        -- %d = digit: finds first two digits, split by comma
        for min, max in string.gmatch(haystackString,'(-%d+),(%d+)') do
            if min ~= nil and max ~= nil then
                return tonumber(min), tonumber(max)
            end
        end
    end
