    -- self.Params = Params
    self.BuildMessage =
      function(commandId, paramId, valueHexString)
          return string.format('%s %s %s %s %s',
              self.SYSEX_PRE,
              tostring(commandId),
              self.ParamIdSysex[paramId],
              tostring(valueHexString),
              self.SYSEX_EOX
